<?php

// Entero (int)
$numero = 18;

// Número con decimales (float)
$flotante = 3.14;

// Texto (string)
$texto = "Hola";

// Verdadero/Falso (boolean)
$booleano = true;

// Valor vacío (null)
$vacio = null;



// Imprime el valor del entero
echo $numero;

// Imprime el valor del float
echo $flotante;

// Imprime el texto
echo $texto;

// gettype() devuelve el tipo de dato de la variable (aquí: boolean)
echo gettype($booleano);

// gettype() devuelve el tipo de dato de la variable (aquí: NULL)
echo gettype($vacio);
